package prelim;
import java.util.NoSuchElementException;
import java.util.Scanner;

public class ExecutableClassMyFixedSizeArrayList {

    /**
     * Property Object class
     */
    public static class Property {

        /** Class Variables */
        private String propertyName;
        private int quantity;
        private String model;

        /**
         * Constructor
         * @param propertyName : String
         * @param quantity : int
         * @param model : String
         */
        public Property(String propertyName, int quantity, String model) {
            this.propertyName = propertyName;
            this.quantity = quantity;
            this.model = model;
        }

        /**
         * Method to get property name
         * @return property name
         */
        public String getPropertyName() {
            return propertyName;
        }

        /**
         * Method to get quantity
         * @return quantity
         */
        public int getQuantity() {
            return quantity;
        }

        /**
         * Method to get model
         * @return model
         */
        public String getModel() {
            return  model;
        }

        /**
         * Method to set property name
         * @param propertyName : String
         */
        public void setPropertyName(String propertyName) {
            this.propertyName = propertyName;
        }

        /**
         * Method to set quantity
         * @param quantity : int
         */
        public void setQuantity(int quantity) {
            this.quantity = quantity;
        }

        /**
         * Method to set model
         * @param model : String
         */
        public void setModel(String model) {
            this.model = model;
        }

        /**
         * Method to return string representation of object
         * @return : string representation
         */
        public String toString() {
            return propertyName + ", " + quantity + ", " + model + ".";
        }
    }

    // Scanner object to be used in accepting user input
    public static Scanner keyboard = new Scanner(System.in);

    // List to hold properties
    public  static MyFixedSizeArrayList<Property> propertiesList = new MyFixedSizeArrayList<Property>();

    /**
     * Main method
     * @param args
     */
    public static void main(String[] args) {
        showIntroduction(); // shows introduction
        do {
            try {
                run();
            } catch (ListOverflowException ex) { // when the number of items in list exceed 5
            } catch (NoSuchElementException ex) { // when an attempt to get an object at an index that does not exist
                System.out.println("A property does not exist at that index.");
            }
        } while (true);
    }

    /**
     * Method to run the program
     * @throws ListOverflowException
     */
    public static void run() throws ListOverflowException {
        logicMenu(showMenu());

    }

    /**
     * Method to show what the program does to the user
     */
    public static void showIntroduction(){
        System.out.println("==============================================================================");
        System.out.println("\t\tThis application is capable of accepting a list of 5 different\n" +
                "\tpersonal properties. The list itself has several different features.");
        System.out.println("==============================================================================");
    }

    /**
     * Method to show menu to user and accept user input
     * Algorithm:
     *      1. Create a do while statement while user input not in the range of menu options
     *      2. Create a try catch statement to handle inputting a string by mistake
     *      3. Display menu options to user
     *      4. Request user input
     *      5. Return user input
     * @return
     */
    public static int showMenu() {
        int input = 0;
        do {
            try {
                System.out.println("Enter the number of what you would like to do:");
                System.out.println("\t1. Add property to list.");
                System.out.println("\t2. Display all properties and their respective information.");
                System.out.println("\t3. Delete property in list");
                System.out.println("\t4. Quit.");
                System.out.print("\t\t -> ");
                input = Integer.parseInt(keyboard.nextLine());
            } catch (NumberFormatException ex) {
                System.out.println("Enter a valid number.");
            }
        } while(input >4 || input <1);
        return input;
    }

    /**
     * Method to handle the logic of the men
     * @param choice : int
     * @throws ListOverflowException
     * Algorithm:
     *      1. Create a switch statement to handle user choice
     *          1.1. if choice = 1 call addProperty method
     *          1.2. If user choice = 2 call propertiesList.display()
     *          1.3. If user choice = 3 call deleteProperty method
     *          1.4. If user choice = 4 call exit method
     */
    public static void logicMenu(int choice) throws ListOverflowException{
        switch (choice) {
            case 1 -> addProperty();
            case 2 -> {
                propertiesList.display();
                System.out.println("Press enter to return to menu.");
                keyboard.nextLine();
            }
            case 3 -> deleteProperty();
            case 4 -> exit();
        }
    }

    /**
     * Method to handle exiting the program
     */
    public static void exit() {
        System.out.println("Thank you!");
        System.exit(0);
    }

    /**
     * Method to handle adding a property
     * Algorithm:
     *      1. Request item name from user and accept input
     *      2. Request and accept quantity of property from user
     *      3. Request and accept model of property from user
     *      4. Create and insert new object to property list
     * @throws ListOverflowException
     */
    public static void addProperty() throws ListOverflowException {
        System.out.println();
        String itemName = requestInput("Enter the property name. ");
        int quantity = 0;
        do {
            try {
                quantity = Integer.parseInt(requestInput("Enter the quantity of the said property."));
            } catch (NumberFormatException ex) {
                System.out.println("Enter a valid number. ");
            }
        } while (quantity <=0);
        String model = requestInput("Enter the model.");
        Property newObject = new Property(itemName,quantity,model);
        propertiesList.insert(newObject);
    }

    /**
     * Method to handle deleting a property
     * Algorithm:
     *      1. Display all properties to user
     *      2. Request and accept user input
     *      3. -1 from user input to compensate for the -1 when indexing
     *      4. Call the delete method of the properties list and pass in index
     */
    public static void deleteProperty() {
        System.out.println("Here are all of the properties in the list:");
        propertiesList.display();
        int index = 0;
        do {
            index = Integer.parseInt(requestInput("Enter the index of the item you would like to delete."));
        } while (index < 0 || index > propertiesList.getSize());
        index--;
        propertiesList.delete(propertiesList.getElementAtIndex(index));
    }

    /**
     * Method to handle requesting user input
     * @param prompt
     * @return
     */
    public static String requestInput(String prompt) {
        System.out.println("\t" + prompt);
        System.out.print("\t\t--> ");
        return keyboard.nextLine();
    }
}

