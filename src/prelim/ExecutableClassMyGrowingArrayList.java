package prelim;

import java.util.Scanner;

public class ExecutableClassMyGrowingArrayList {

    /** Student Object */
    public static class Student{

        // Class variables
        private String name;
        private String id;
        private String course;

        /**
         * Constructor
         * @param name
         * @param id
         * @param course
         */
        public Student(String name, String id, String course) {
            this.name = name;
            this.id = id;
            this.course = course;
        }

        /**
         * Getter
         * @return name
         */
        public String getName(){
            return name;
        }

        /**
         * Getter
         * @return id
         */
        public String getId(){
            return id;
        }

        /**
         * Getter
         * @return course
         */
        public String getCourse(){
            return course;
        }

        /**
         * Setter
         * @param name : String
         */
        public void setName(String name) {
            this.name = name;
        }

        /**
         * Setter
         * @param id : String
         */
        public void setId(String id) {
            this.id = id;
        }

        /**
         * Setter
         * @param course : String
         */
        public void setCourse(String course) {
            this.course = course;
        }

        /**
         * String representation of object
         * @return
         */
        @Override
        public String toString() {
            return name + ", " + id + ", " + course;
        }
    }

    // List to hold all Students
    public static MyGrowingArrayList<Student> studentList = new MyGrowingArrayList<Student>();

    // Scanner object to accept user input
    public static Scanner keyboard = new Scanner(System.in);

    /**
     * Main method
     * @param args
     */
    public static void main(String[] args) {
        showIntroduction();
        do {
            try {
                run();
            } catch (ListOverflowException ex) {
            }
        }while (true);
    }

    /**
     * Method to show program instructions
     */
    public static void showIntroduction(){
        System.out.println("===================================================================================");
        System.out.println("\t\tThis application is capable of accepting a list of student names and\n" +
                "\ttheir pertinent information. The list itself has several different features.");
        System.out.println("===================================================================================");
    }

    /**
     * Method to handle logic of program
     * @throws ListOverflowException
     * Algorithm:
     *      1. Show menu to user
     *      2. Accept user choice and call corresponding method
     */
    public static void run() throws ListOverflowException{
        System.out.println("Currently the maximum size of the list is " + studentList.getSize() + ".");
        int choice = showMenu();
        switch (choice) {
            case 1 -> addStudent();
            case 2 -> display();
            case 3 -> deleteStudent();
            case 4 -> exit();
        }
    }

    /**
     * Method to show menu to user and accept input
     * Algorithm:
     *      1. show menu to user
     *      2. Accept user choice in range of menu options
     *      3. Return user choice
     * @return choice
     */
    public static int showMenu() {
        int input = 0;
        do {
            try {
                System.out.println("Enter the number of what you would like to do:");
                System.out.println("\t1. Add a student to list.");
                System.out.println("\t2. Display all students and their respective information.");
                System.out.println("\t3. Delete a student in list.");
                System.out.println("\t4. Quit.");
                System.out.print("\t\t -> ");
                input = Integer.parseInt(keyboard.nextLine());
            } catch (NumberFormatException ex) {
                System.out.println("Enter a valid number.");
            }
        } while(input >4 || input <1);
        return input;
    }

    /**
     * Method to add student to list
     * Algorithm:
     *      1. Accept Student information
     *      2. Create Student object and return it
     * @throws ListOverflowException
     */
    public static void addStudent() throws ListOverflowException {
        System.out.println();
        String studName = requestInput("Enter the student name. ");
        String studID = requestInput("Enter the student's ID number.");
        String studCourse = requestInput("Enter the student's course.");
        Student newStudent = new Student(studName,studID,studCourse);
        studentList.insert(newStudent);
    }

    /**
     * Method to accept user input
     * @param prompt
     * @return
     */
    public static String requestInput(String prompt) {
        System.out.println("\t" + prompt);
        System.out.print("\t\t--> ");
        return keyboard.nextLine();
    }

    /**
     * Method to display students to user
     */
    public static void display() {
        studentList.display();
        System.out.println("Press enter to return to menu.");
        keyboard.nextLine();
    }

    /**
     * Method to delete Student object
     * Algorithm:
     *      1. Request student index to delete
     *      2. Call delete method of studentList and pass in index
     */
    public static void deleteStudent() {
        System.out.println("Here are all of the students in the list:");
        studentList.display();
        int index = 0;
        do {
            index = Integer.parseInt(requestInput("Enter the index of the item you would like to delete."));
        } while (index < 0 || index > studentList.getSize());
        index -= 1;
        studentList.delete(studentList.getElementAtIndex(index));
    }

    /**
     * Method to exit program
     */
    public static void exit() {
        System.out.println("Thank you!");
        System.exit(0);
    }
}
